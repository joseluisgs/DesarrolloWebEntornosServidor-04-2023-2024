<?php

namespace Calculator;

class Calculator
{
    /**
     * Suma dos números.
     *
     * @param int|float $a Primer número a sumar.
     * @param int|float $b Segundo número a sumar.
     * @return int|float La suma de $a y $b.
     */
    public function add($a, $b)
    {
        return $a + $b;
    }
}